import com.j256.ormlite.field.DatabaseField;
import com.j256.ormlite.table.DatabaseTable;

/**
 * Created by Administrator on 2016/11/1 0001.
 */
@DatabaseTable(tableName = "t_user")
public class UserInfo {
    @DatabaseField(columnName = "_id",generatedId = true)
    long _id;
    @DatabaseField(columnName = "usernumber",canBeNull = false)
    String usernumeber;
    @DatabaseField(columnName = "nickname",canBeNull = false)
    String nikeName;
    @DatabaseField(columnName = "password")
    String password;
    //数据库名字
    @DatabaseField(columnName = "dbname",canBeNull = false)
    String dbName;

    public String getUsernumeber() {
        return usernumeber;
    }

    public void setUsernumeber(String usernumeber) {
        this.usernumeber = usernumeber;
    }

    public long get_id() {
        return _id;
    }

    public void set_id(long _id) {
        this._id = _id;
    }

    public String getNikeName() {
        return nikeName;
    }

    public void setNikeName(String nikeName) {
        this.nikeName = nikeName;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getDbName() {
        return dbName;
    }

    public void setDbName(String dbName) {
        this.dbName = dbName;
    }
}
